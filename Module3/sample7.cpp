/******************************************************************************
Opeators 
+,-,*,/,%
*******************************************************************************/
#include <iostream>
using namespace std;
int main()
{
    int a = 15;
    int b = 4;
    cout << "a + b " << (a + b) << endl;
    cout << "a - b " << (a - b) << endl;
    cout << "a * b " << (a * b) << endl;
    cout << "a / b " << (a / b) << endl;
    cout << "a % b " << (a % b) << endl;
    a=10;
    b=5;
    cout << "a++ " << ++a << endl;
    cout << "b-- " << --b << endl;
    
    return 0;
}
